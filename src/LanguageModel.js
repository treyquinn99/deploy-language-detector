var matrices = {"English" : english, "Spanish" : spanish, "French" : french};

class LanguageModel {
     #name;
     #matrix;

     /**
     * Constructor
     * Processes corpus file and creates matrix of transition probabilities
     * between letters.
     * @param modelName Title of model.
     */

     constructor(modelName) {
         this.#name = modelName;
         this.#matrix = matrices[modelName];
     }
     
     /**
     * @return String with current LanguageModel instance's name.
     */

    getName() {
         return this.#name;
     }

     /**
     * @param test String to analyze and calculate the probability that
     * it matches the current LanguageModel.
     * @return double representing the probability that the String passed
     * in matches the current LanguageModel.
     */

     probability(test) {
         let matchProbability = 1.0;
         let splitLine = test.split(" ")

         let str;
        for (str of splitLine) {
            for (let i = 0; i < str.length; i++) {
                let charCode = str.charCodeAt(i);
                if ((charCode < 65 || charCode > 90) && (charCode < 97 || charCode > 122)) {
                    str = str.substring(0, i) + str.substring(i + 1);
                    i--;
                }
            }

            for (let i = 0; i < str.length - 1; i++) {
                let r = str.charCodeAt(i);
                let c = str.charCodeAt(i + 1);

                if (!(r < 65 || r > 90)) {
                    r -= 65;
                }
                else {
                    r -= 97;
                }

                if (!(c < 65 || c > 90)) {
                    c -= 65;
                }
                else {
                    c -= 97;
                }

                matchProbability *= this.#matrix[r][c];
            }
        }

        return matchProbability;
     }
 }

 export default LanguageModel;